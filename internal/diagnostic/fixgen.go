func genFixConversationDrift(p Problem, r *InspectReport, paths ProviderPaths, _ session.ProviderName) []Artefact {
	pat := r.Patterns
	if pat == nil {
		return nil
	}

	var observations []string
	if pat.UserCorrectionCount >= 5 {
		observations = append(observations, fmt.Sprintf("%d user corrections", pat.UserCorrectionCount))
	}
	if pat.LongestRunLength >= 30 {
		observations = append(observations, fmt.Sprintf("longest unguided run: %d messages", pat.LongestRunLength))
	}

	patch := fmt.Sprintf(`
## Conversation Focus (generated by aisync inspect)

Session analysis: %s.

Rules:
- After every 10-15 tool calls, briefly state what you accomplished and what remains
- If the user corrects you, acknowledge the correction and re-state the updated goal
- Do not explore tangential issues unless explicitly asked
- For long implementation tasks, outline the plan first, then execute step by step
- If unsure about approach, ask the user rather than exploring multiple paths
`, strings.Join(observations, ", "))

	return []Artefact{{
		Kind:        ArtefactAgentInstructions,
		Description: "Conversation focus — reduce drift and corrections",
		RelPath:     paths.InstructionsFile,
		Content:     patch,
		AppendTo:    true,
	}}
}

