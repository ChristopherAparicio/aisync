// fixGenerator is a function that produces artefacts for a specific problem.
type fixGenerator func(problem Problem, report *InspectReport, paths ProviderPaths, provider session.ProviderName) []Artefact

// registry maps problem IDs to their fix generators.
var registry = map[ProblemID]fixGenerator{
	ProblemExpensiveScreenshots: genFixExpensiveScreenshots,
	ProblemOversizedScreenshots: genFixOversizedScreenshots,
	ProblemUnresizedScreenshots: genFixUnresizedScreenshots,
	ProblemFrequentCompaction:   genFixFrequentCompaction,
	ProblemContextNearLimit:     genFixContextNearLimit,
	ProblemVerboseCommands:      genFixVerboseCommands,
	ProblemRepeatedCommands:     genFixRepeatedCommands,
	ProblemToolErrorLoops:       genFixToolErrorLoops,
	ProblemHighInputRatio:       genFixHighInputRatio,
	ProblemYoloEditing:          genFixYoloEditing,
	ProblemConversationDrift:    genFixConversationDrift,
	ProblemContextThrashing:     genFixContextThrashing,

	// RTK module
	ProblemRTKCurlConflict:    genFixRTKCurlConflict,
	ProblemRTKSecretRedaction: genFixRTKSecretRedaction,
	ProblemRTKIdenticalRetry:  genFixRTKIdenticalRetry,

	// API module
	ProblemAPIRetryLoop:          genFixAPIRetryLoop,
	ProblemIdenticalCommandBurst: genFixIdenticalCommandBurst,
}

// GenerateFixes produces provider-specific artefacts for all detected problems.
func GenerateFixes(report *InspectReport, provider session.ProviderName) *FixSet {
	paths := ProviderPathsFor(provider)

	fs := &FixSet{
		SessionID: report.SessionID,
		Provider:  string(provider),
	}

	for _, problem := range report.Problems {
		gen, ok := registry[problem.ID]
		if !ok {
			continue // no fix generator for this problem
		}
		artefacts := gen(problem, report, paths, provider)
		if len(artefacts) == 0 {
			continue
		}
		// Collect documentation links for the artefact kinds in this fix.
		docLinks := ProviderDocLinks(provider)
		seen := make(map[string]bool)
		for _, a := range artefacts {
			for _, dl := range ArtefactDocLinks(provider, a.Kind) {
				if !seen[dl.URL] {
					seen[dl.URL] = true
					docLinks = append(docLinks, dl)
				}
			}
		}
		// Dedup provider-level links that also appeared as artefact-level.
		var uniqueLinks []DocLink
		linkSet := make(map[string]bool)
		for _, dl := range docLinks {
			if !linkSet[dl.URL] {
				linkSet[dl.URL] = true
				uniqueLinks = append(uniqueLinks, dl)
			}
		}

		fs.Fixes = append(fs.Fixes, ProblemFix{
			ProblemID:    problem.ID,
			ProblemTitle: problem.Title,
			Severity:     problem.Severity,
			Artefacts:    artefacts,
			DocLinks:     uniqueLinks,
		})
	}

	return fs
}

// ── Image fix generators ────────────────────────────────────────────────────

func genFixExpensiveScreenshots(p Problem, r *InspectReport, paths ProviderPaths, provider session.ProviderName) []Artefact {
	var arts []Artefact

	// 1. Capture script
	arts = append(arts, genCaptureScript(paths, provider))

	// 2. Agent instructions patch
	patch := screenshotInstructionsPatch(r)
	arts = append(arts, Artefact{
		Kind:        ArtefactAgentInstructions,
		Description: "Screenshot protocol — describe-and-discard, resolution limits, JPEG preference",
		RelPath:     paths.InstructionsFile,
		Content:     patch,
		AppendTo:    true,
	})

	// 3. Provider-specific skill/command
	arts = append(arts, genProviderSpecificScreenshot(paths, provider)...)

	return arts
}

