// ── Command fix generators ──────────────────────────────────────────────────

func genFixVerboseCommands(p Problem, r *InspectReport, paths ProviderPaths, _ session.ProviderName) []Artefact {
	cmd := r.Commands
	if cmd == nil {
		return nil
	}

	var topCmdLines strings.Builder
	for _, c := range cmd.TopByOutput {
		if c.TotalBytes < 10_000 {
			break
		}
		topCmdLines.WriteString(fmt.Sprintf("- `%s`: %s total across %d calls\n",
			c.Command, fmtBytes(c.TotalBytes), c.Invocations))
	}

	patch := fmt.Sprintf(`
## Command Output Limits (generated by aisync inspect)

Session analysis: %s total command output across %d commands.

Top commands by output volume:
%s
Rules:
- Pipe verbose commands through tail -50 or head -50
- For test output: use --no-coverage and capture only failures
- For build output: redirect to a file and read only on failure
- Never run unbounded cat on files > 200 lines — use read with offset/limit
- For grep/search: limit results with -m 20 (max matches)
`, fmtBytes(cmd.TotalOutputBytes), cmd.TotalCommands, topCmdLines.String())

	return []Artefact{{
		Kind:        ArtefactAgentInstructions,
		Description: "Command output limits — reduce verbose tool output",
		RelPath:     paths.InstructionsFile,
		Content:     patch,
		AppendTo:    true,
	}}
}

