// genCaptureScript produces the optimized capture-screen.sh script.
func genCaptureScript(paths ProviderPaths, provider session.ProviderName) Artefact {
	scriptDir := paths.ScriptsDir
	if scriptDir == "" {
		scriptDir = "scripts/"
	}
	return Artefact{
		Kind:        ArtefactScript,
		Description: "Optimized screenshot capture — 500px max, JPEG Q50, ~66% token reduction",
		RelPath:     filepath.Join(scriptDir, "capture-screen.sh"),
		Content: `#!/bin/bash
# Generated by aisync inspect — optimized screenshot capture
# Reduces token cost by ~66% (1500 → ~500 tokens per image)
#
# Usage: ./capture-screen.sh [output-path]
# Default output: /tmp/screenshot.jpg

set -euo pipefail

DEVICE="${DEVICE_ID:-booted}"
OUT="${1:-/tmp/screenshot.jpg}"
RAW=$(mktemp /tmp/aisync-capture-XXXXXX.png)

# Capture
xcrun simctl io "$DEVICE" screenshot "$RAW" 2>/dev/null

# Resize to 500px max dimension
sips -Z 500 "$RAW" --out "$RAW" >/dev/null 2>&1

# Convert to JPEG Q50 for minimum token cost
sips -s format jpeg -s formatOptions 50 "$RAW" --out "$OUT" >/dev/null 2>&1

# Cleanup
rm -f "$RAW"

echo "$OUT"
`,
	}
}

// screenshotInstructionsPatch produces the agent instructions section for screenshot handling.
func screenshotInstructionsPatch(r *InspectReport) string {
	var sb strings.Builder
	sb.WriteString("\n## Screenshot Protocol (generated by aisync inspect)\n\n")

	if r.Images != nil && r.Images.EstImageCost > 0 {
		sb.WriteString(fmt.Sprintf("Session analysis: %d images, avg %.0f turns in context, est. cost $%.2f.\n\n",
			r.Images.ToolReadImages, r.Images.AvgTurnsInCtx, r.Images.EstImageCost))
	}

	sb.WriteString(`Rules:
- Use ./capture-screen.sh (or scripts/capture-screen.sh) instead of raw simctl + sips
- After reading a screenshot, describe what you see in 2-3 sentences of text
- Never re-read a screenshot you already described in the last 10 messages
- Prefer JPEG over PNG for all screenshots
- Target resolution: 500px max dimension (not 1000px)
- If a screenshot shows the expected result, say so and move on — do not re-capture
`)
	return sb.String()
}

// genProviderSpecificScreenshot generates skill/command artefacts specific to the provider.
func genProviderSpecificScreenshot(paths ProviderPaths, provider session.ProviderName) []Artefact {
	switch provider {
	case session.ProviderOpenCode:
		if paths.SkillsDir == "" {
			return nil
		}
		skillDir := filepath.Join(paths.SkillsDir, "screenshot-capture")
		return []Artefact{{
			Kind:        ArtefactSkill,
			Description: "OpenCode skill — screenshot capture with optimized settings",
			RelPath:     filepath.Join(skillDir, "SKILL.md"),
			Content: `# Screenshot Capture Skill

Capture iOS simulator screenshots optimized for token economy.

## Usage

Run the capture script:
` + "```bash\n./capture-screen.sh [output-path]\n```" + `

## Rules

- Always use this skill instead of raw ` + "`xcrun simctl io ... screenshot`" + `
- Output is JPEG at 500px max dimension (~500 tokens vs ~1500 for PNG at 1000px)
- After reading the screenshot, describe what you see — do not keep it in context
`,
		}}

	case session.ProviderClaudeCode:
		if paths.CommandsDir == "" {
			return nil
		}
		return []Artefact{{
			Kind:        ArtefactCommand,
			Description: "Claude Code command — capture and read simulator screenshot",
			RelPath:     filepath.Join(paths.CommandsDir, "capture-screenshot.md"),
			Content: `# Capture Screenshot

Capture an iOS simulator screenshot optimized for token economy.

Steps:
1. Run: ` + "`bash scripts/capture-screen.sh /tmp/screenshot.jpg`" + `
2. Read the resulting JPEG file
3. Describe what you see in 2-3 sentences
4. Do not re-read the same screenshot
`,
		}}

	default:
		return nil
	}
}

